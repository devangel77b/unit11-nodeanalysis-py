#!/usr/bin/env python3

import numpy as np

A = np.array([
    [-3,1,0,1,0,1,0,0],
    [1,-3,1,0,0,0,1,0],
    [0,1,-3,1,0,0,0,1],
    [1,0,1,-3,1,0,0,0],
    [0,0,0,1,-3,1,0,1],
    [1,0,0,0,1,-3,1,0],
    [0,1,0,0,0,1,-3,1],
    [0,0,0,0,0,0,0,1]])
b = np.array([[-1,0,0,0,0,0,0,0]]).transpose()
x = np.dot(np.linalg.inv(A),b)
print("R_eq = {0} ohm".format(x[0,0]))
